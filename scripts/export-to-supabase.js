// Reads the live SQLite database and generates supabase/schema.sql:
// Postgres schema + RLS policies + INSERTs of all current data.
import Database from 'better-sqlite3'
import fs from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const db = new Database(path.join(__dirname, '..', 'data', 'tracker.db'), { readonly: true })

const q = v => v === null || v === undefined ? 'NULL' : `'${String(v).replace(/'/g, "''")}'`
const qj = v => `${q(v)}::jsonb`
const qb = v => (v ? 'true' : 'false')

let sql = `-- Marketing Task Tracker: Supabase schema + data migration
-- Generated ${new Date().toISOString()} from the live SQLite database.
-- Paste this whole file into the Supabase SQL Editor and click Run.

create table members (
  id bigint generated by default as identity primary key,
  name text not null unique,
  role text not null default 'member'
);

create table projects (
  id bigint generated by default as identity primary key,
  name text not null,
  sort_order int not null default 0,
  created_at timestamptz not null default now()
);

create table tasks (
  id bigint generated by default as identity primary key,
  project_id bigint not null references projects(id) on delete cascade,
  title text not null,
  owners jsonb not null default '[]',
  assigned_by text,
  status text not null default 'Not yet started',
  reviewed boolean not null default false,
  assigned_date date,
  start_date date,
  end_date date,
  golive_date date,
  notes text,
  sort_order int not null default 0,
  created_at timestamptz not null default now()
);

create table comments (
  id bigint generated by default as identity primary key,
  task_id bigint not null references tasks(id) on delete cascade,
  author text not null,
  text text not null,
  created_at timestamptz not null default now()
);

create table notifications (
  id bigint generated by default as identity primary key,
  recipient text not null,
  actor text,
  task_id bigint references tasks(id) on delete cascade,
  type text not null,
  message text not null,
  is_read boolean not null default false,
  created_at timestamptz not null default now()
);

-- Row Level Security: only signed-in team members can touch anything.
alter table members enable row level security;
alter table projects enable row level security;
alter table tasks enable row level security;
alter table comments enable row level security;
alter table notifications enable row level security;

create policy "team full access" on members for all to authenticated using (true) with check (true);
create policy "team full access" on projects for all to authenticated using (true) with check (true);
create policy "team full access" on tasks for all to authenticated using (true) with check (true);
create policy "team full access" on comments for all to authenticated using (true) with check (true);
create policy "team full access" on notifications for all to authenticated using (true) with check (true);

`

for (const m of db.prepare('SELECT * FROM members ORDER BY id').all()) {
  sql += `insert into members (id, name, role) values (${m.id}, ${q(m.name)}, ${q(m.role)});\n`
}
sql += '\n'
for (const p of db.prepare('SELECT * FROM projects ORDER BY id').all()) {
  sql += `insert into projects (id, name, sort_order, created_at) values (${p.id}, ${q(p.name)}, ${p.sort_order}, ${q(p.created_at)});\n`
}
sql += '\n'
for (const t of db.prepare('SELECT * FROM tasks ORDER BY id').all()) {
  sql += `insert into tasks (id, project_id, title, owners, assigned_by, status, reviewed, assigned_date, start_date, end_date, golive_date, notes, sort_order, created_at) values (` +
    `${t.id}, ${t.project_id}, ${q(t.title)}, ${qj(t.owners)}, ${q(t.assigned_by)}, ${q(t.status)}, ${qb(t.reviewed)}, ` +
    `${q(t.assigned_date)}, ${q(t.start_date)}, ${q(t.end_date)}, ${q(t.golive_date)}, ${q(t.notes)}, ${t.sort_order}, ${q(t.created_at)});\n`
}
sql += '\n'
for (const c of db.prepare('SELECT * FROM comments ORDER BY id').all()) {
  sql += `insert into comments (id, task_id, author, text, created_at) values (${c.id}, ${c.task_id}, ${q(c.author)}, ${q(c.text)}, ${q(c.created_at)});\n`
}
sql += '\n'
for (const n of db.prepare('SELECT * FROM notifications ORDER BY id').all()) {
  sql += `insert into notifications (id, recipient, actor, task_id, type, message, is_read, created_at) values (` +
    `${n.id}, ${q(n.recipient)}, ${q(n.actor)}, ${n.task_id ?? 'NULL'}, ${q(n.type)}, ${q(n.message)}, ${qb(n.is_read)}, ${q(n.created_at)});\n`
}

sql += `
-- keep the id counters ahead of the imported rows
select setval(pg_get_serial_sequence('members','id'), coalesce((select max(id) from members), 1));
select setval(pg_get_serial_sequence('projects','id'), coalesce((select max(id) from projects), 1));
select setval(pg_get_serial_sequence('tasks','id'), coalesce((select max(id) from tasks), 1));
select setval(pg_get_serial_sequence('comments','id'), coalesce((select max(id) from comments), 1));
select setval(pg_get_serial_sequence('notifications','id'), coalesce((select max(id) from notifications), 1));
`

const outDir = path.join(__dirname, '..', 'supabase')
fs.mkdirSync(outDir, { recursive: true })
fs.writeFileSync(path.join(outDir, 'schema.sql'), sql)
console.log('Wrote supabase/schema.sql with',
  db.prepare('SELECT COUNT(*) n FROM projects').get().n, 'projects and',
  db.prepare('SELECT COUNT(*) n FROM tasks').get().n, 'tasks.')
